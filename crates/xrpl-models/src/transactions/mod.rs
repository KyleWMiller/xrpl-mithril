///
/// For transaction types without a dedicated struct (e.g., newer amendment-gated
/// types), the `Unknown` variant preserves the full JSON map.
///
/// # Examples
///
/// ```
/// # use serde_json::json;
/// # use xrpl_models::transactions::Transaction;
/// let json = json!({
///     "TransactionType": "Payment",
///     "Account": "rHb9CJAWyB4rj91VRWn96DkukG4bwdtyTh",
///     "Fee": "12",
///     "Sequence": 1,
///     "Destination": "rPT1Sjq2YGrBMTttX4GZHjKu9dyfzbpAYe",
///     "Amount": "1000000"

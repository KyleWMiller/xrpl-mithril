    #[test]
    fn round_trip_payment() {
        let json = json!({
            "TransactionType": "Payment",
            "Account": "rHb9CJAWyB4rj91VRWn96DkukG4bwdtyTh",
            "Fee": "12",
            "Sequence": 1,
            "Destination": "rPT1Sjq2YGrBMTttX4GZHjKu9dyfzbpAYe",
            "Amount": "1000000"
        });

        let tx: Transaction = serde_json::from_value(json.clone()).expect("deser");
        let serialized = serde_json::to_value(&tx).expect("ser");

        // Verify key fields survived the round-trip
        let map = serialized.as_object().expect("obj");
        assert_eq!(map.get("TransactionType").and_then(|v| v.as_str()), Some("Payment"));
        assert_eq!(map.get("Account").and_then(|v| v.as_str()), Some("rHb9CJAWyB4rj91VRWn96DkukG4bwdtyTh"));
        assert_eq!(map.get("Sequence").and_then(|v| v.as_u64()), Some(1));
        assert_eq!(map.get("Amount").and_then(|v| v.as_str()), Some("1000000"));
        assert_eq!(map.get("Destination").and_then(|v| v.as_str()), Some("rPT1Sjq2YGrBMTttX4GZHjKu9dyfzbpAYe"));
    }


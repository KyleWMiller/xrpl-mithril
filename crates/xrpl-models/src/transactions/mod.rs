/// Fields common to all transactions.
///
/// Every transaction on the XRPL includes these fields. Type-specific fields
/// are defined in each transaction's own struct.
#[derive(Debug, Clone, PartialEq, Serialize, Deserialize)]
pub struct TransactionCommon {
    /// The unique address of the account that initiated the transaction.
    #[serde(rename = "Account")]
    pub account: AccountId,

    /// The XRP fee to destroy (in drops).
    #[serde(rename = "Fee")]
    pub fee: Amount,

    /// The sequence number of the account. 0 if using Tickets.
    #[serde(rename = "Sequence")]
    pub sequence: u32,

    /// Bit-flags for this transaction.
    #[serde(rename = "Flags", default, skip_serializing_if = "Option::is_none")]
    pub flags: Option<u32>,

    /// Highest ledger index this transaction can appear in.
    #[serde(
        rename = "LastLedgerSequence",
        default,
        skip_serializing_if = "Option::is_none"
    )]
    pub last_ledger_sequence: Option<u32>,

    /// Hash of a previous transaction from this account.
    #[serde(rename = "AccountTxnID", default, skip_serializing_if = "Option::is_none")]
    pub account_txn_id: Option<Hash256>,

    /// Array of memo objects.
    #[serde(rename = "Memos", default, skip_serializing_if = "Option::is_none")]
    pub memos: Option<Vec<Memo>>,

    /// Network ID for non-mainnet chains.
    #[serde(rename = "NetworkID", default, skip_serializing_if = "Option::is_none")]
    pub network_id: Option<u32>,

    /// Arbitrary source tag.
    #[serde(rename = "SourceTag", default, skip_serializing_if = "Option::is_none")]
    pub source_tag: Option<u32>,

    /// The public key corresponding to the signing key.
    #[serde(rename = "SigningPubKey", default, skip_serializing_if = "Option::is_none")]
    pub signing_pub_key: Option<Blob>,

    /// The transaction signature.
    #[serde(rename = "TxnSignature", default, skip_serializing_if = "Option::is_none")]
    pub txn_signature: Option<Blob>,

    /// Ticket to use instead of Sequence.
    #[serde(
        rename = "TicketSequence",
        default,
        skip_serializing_if = "Option::is_none"
    )]
    pub ticket_sequence: Option<u32>,

    /// Array of multi-sign Signers.
    #[serde(rename = "Signers", default, skip_serializing_if = "Option::is_none")]
    pub signers: Option<Vec<Signer>>,
}

/// A memo attached to a transaction.
#[derive(Debug, Clone, PartialEq, Serialize, Deserialize)]
pub struct Memo {
    /// The type of memo (conventionally a MIME type).
    #[serde(rename = "MemoType", default, skip_serializing_if = "Option::is_none")]
    pub memo_type: Option<Blob>,

    /// The memo content.
    #[serde(rename = "MemoData", default, skip_serializing_if = "Option::is_none")]
    pub memo_data: Option<Blob>,

    /// The memo format.
    #[serde(rename = "MemoFormat", default, skip_serializing_if = "Option::is_none")]
    pub memo_format: Option<Blob>,
}

/// A signer in a multi-signed transaction.
#[derive(Debug, Clone, PartialEq, Serialize, Deserialize)]
pub struct Signer {
    /// The signer's account.
    #[serde(rename = "Account")]
    pub account: AccountId,

    /// The signer's signature.
    #[serde(rename = "TxnSignature")]
    pub txn_signature: Blob,

    /// The signer's public key.
    #[serde(rename = "SigningPubKey")]
    pub signing_pub_key: Blob,
}

/// Helper macro for implementing common() and common_mut() across all variants.
macro_rules! impl_transaction_accessors {
    ($($variant:ident),+ $(,)?) => {
        /// Get a reference to the common fields shared by all transaction types.
        pub fn common(&self) -> &TransactionCommon {
            match self {
                $(Transaction::$variant { common, .. } => common,)+
                Transaction::Unknown { common, .. } => common,
            }
        }

        /// Get a mutable reference to the common fields.
        pub fn common_mut(&mut self) -> &mut TransactionCommon {
            match self {
                $(Transaction::$variant { common, .. } => common,)+
                Transaction::Unknown { common, .. } => common,
            }
        }

        /// Get the transaction type discriminator.
        pub fn transaction_type(&self) -> &str {
            match self {
                $(Transaction::$variant { .. } => stringify!($variant),)+
                Transaction::Unknown { transaction_type, .. } => transaction_type,
            }
        }
    };
}

/// A fully-typed XRPL transaction combining common and type-specific fields.
